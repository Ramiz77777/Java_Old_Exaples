package lesson3.constructor;

public class Person {
    String name;
    String surname;
    String haircolor;
    int age;
    double tall;

    public Person(String name, String surname, String haircolor, int age, double tall) {
        this.name = name;
        this.surname = surname;
        this.haircolor = haircolor;
        this.age = age;
        this.tall = tall;
    }
}






